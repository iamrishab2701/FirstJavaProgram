package ControlStatements;

public class ForLoop {

    public static void main(String args[])
    {
        //print number 1 to 10

        for (int i = 1;i<=10;i++)
        {
            System.out.println(i);
        }

        //print in descending order

        for(int i = 10;i>=1;i--)
        {
            System.out.println(i);
        }
    }
}