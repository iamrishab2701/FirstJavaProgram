package Revision;

public class SwitchCase {
    public static void main(String[] args) {

        String Time = "12";

        switch (Time)
        {
            case "6":
            {
                System.out.println("Morning or Evening");
            }

            case "12":
            {
                System.out.println("Night or Afternoon");
            }
        }
    }
}
