package Revision;

public class DataType {

    public static void main(String[] args) {

        //Premitive Data Types

        int a=10;
        char b='T';
        boolean c= false;
        float d = 10;
        double e = 10.5;

        //Derived Data types

        int f[]={1,2,3}; //single dimensional array
        String g[][]= {{"Apple","Orange"},{"Papaya","Lemon"}}; //Multidimensional Array
        String h = "This is String";
    }
}
